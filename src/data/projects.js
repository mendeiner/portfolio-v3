import videoManifest from 'virtual:video-manifest'

const BASE = import.meta.env.BASE_URL

function videos(slug) {
  const paths = videoManifest[slug] || []
  return {
    videoSrc: paths[0] ?? null,
    slides: paths.map(v => ({ videoSrc: v })),
  }
}

